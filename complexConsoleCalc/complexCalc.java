public class complexCalc implements complexOperations
{
  public String complexAddition(String a,String b)
  {
    return a+"+";
  }
  public String complexSubtraction(String a,String b)
  {
    return a+"-";
  }
  public String complexMultiplication(String a,String b)
  {
    return a+"*";
  }
  public String complexDivision(String a,String b)
  {
    return a+"/";
  }
}
